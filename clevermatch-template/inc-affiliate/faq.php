		<h2 class="accordion-header">
			<button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseEight" aria-expanded="false" aria-controls="collapseEight">
				Wie berechnet sich meine Provision? 
			</button>
		</h2>
		<div id="collapseEight" class="accordion-collapse collapse" data-bs-parent="#accordionFAQ">
			<div class="accordion-body">
				Bei von Ihnen empfohlenen Arbeitgebern fällt die Provision an, sobald dem Arbeitgeber ein Mitarbeiter durch uns vermittelt wurde. Sie erfolgt innerhalb von 14 Tagen nach Vertragsunterzeichnung und beträgt 15 % des Personalberatungshonorars. Das Personalberatungshonorar liegt im Durchschnitt bei 25 bis 33 % des Jahreszielgehalts des platzierten Mitarbeiters. <br><br>
				Beispiel Arbeitgeber: Zielgehalt = 90.000 €, Personalberatungshonorar = 30.000 €, Ihre Prämie = 4.500 €.<br><br>

				Bei empfohlenen Mitarbeitern richtet sich die Höhe der Prämie nach der ausgeschriebenen Provision, die direkt bei den Vakanzen auf unserer Website ausgewiesen ist. Sie wissen bereits im Voraus, welche Vergütung Sie für Ihre Empfehlung erwartet.<br>
				Sofern kein fester Betrag genannt ist, beträgt die Provision 10% des Personalberatungshonorars, welches im Durchschnitt 25 bis 33% des Jahreszielgehalts des Mitarbeiters beträgt.<br><br>
				Beispiel Mitarbeiter: Zielgehalt = 90.000 €, Personalberatungshonorar = 30.000 €, Ihre Provision = 3.000 €.
			</div>
		</div>
